
public class Arithmeticoperation {
	private int n1;
	private int n2;
	public Arithmeticoperation(int n1, int n2) {
		super();
		this.n1 = n1;
		this.n2 = n2;
	}
	public void addData() {
		int add=n1+n2;
		System.out.println("sum is:"+add);
		
	}
	public void subData() {
		int sub=n1-n2;
		System.out.println("sub is:"+sub);
		
	}
	
}
