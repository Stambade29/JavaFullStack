
public class Fruitstall {
	public String Stallname;
	public String Stalladdr;
	public String Stallitem;
	public Fruitstall(String stallname, String stalladdr, String stallitem) {
		super();
		Stallname = stallname;
		Stalladdr = stalladdr;
		Stallitem = stallitem;
	}
	public String getStallname() {
		return Stallname;
	}
	public void setStallname(String stallname) {
		Stallname = stallname;
	}
	public String getStalladdr() {
		return Stalladdr;
	}
	public void setStalladdr(String stalladdr) {
		Stalladdr = stalladdr;
	}
	public String getStallitem() {
		return Stallitem;
	}
	public void setStallitem(String stallitem) {
		Stallitem = stallitem;
	}
	
	

}
